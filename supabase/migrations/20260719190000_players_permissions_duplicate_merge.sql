create or replace function private.has_permission(p_permission text)
returns boolean language sql stable security definer set search_path = pg_catalog, public, private as $$
  select exists (
    select 1 from public.profile_admin_roles par
    join public.admin_role_permissions arp on arp.role_id = par.role_id
    join public.admin_permissions permission on permission.id = arp.permission_id
    join public.profiles profile on profile.id = par.profile_id
    where profile.auth_user_id = auth.uid() and permission.name = p_permission
  ) or exists (
    select 1 from public.profile_roles pr join public.profiles profile on profile.id = pr.profile_id
    where profile.auth_user_id = auth.uid() and pr.role in ('super_admin','administrator')
  )
$$;

